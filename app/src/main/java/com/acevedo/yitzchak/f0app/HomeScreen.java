        String fullName = intent.getStringExtra(MainActivity.EXTRA_FULL);
        TextView toDisplayName = (TextView)findViewById(R.id.displayName);
        toDisplayName.setText(fullName);

        FloatingActionButton fab = (FloatingActionButton) findViewById(R.id.fab);
